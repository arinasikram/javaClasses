package Class7;

public class DWhile {
	public static void main(String[] args){
		
		int num=1;
		while(num<=7) {
			
			
			System.out.println("hello");
			num++;
			int num1=10;
			do {
				System.out.println("Bye");
				num1++;
			}while (num1<=7);
			System.out.println("******************");
			
		// print even numbers from 20 to 50 using do while
			
			int a=20;
			while(a<=50) {
				System.out.println(a);
				int a1=30;
				do {
				System.out.println(a1);
				
				
				a1++;
				}while (a1<=20);
				System.out.println("*****************");
				
				
				}
				
			
		}
		
	}

}
