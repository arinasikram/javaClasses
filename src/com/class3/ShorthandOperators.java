package com.class3;

public class ShorthandOperators {
	public static void main(String[]args) {
		
		
	int num4=100;
	num4+=67;//num4=num4+67
	System.out.println(num4);
	
	int cakePieces=11;
	cakePieces/=4;
		System.out.println(cakePieces);
				
	
	
	
	int cakePieces2=25;
	cakePieces2%=7;
	System.out.println(cakePieces2);
	
	
	
	
	
}
	

}
